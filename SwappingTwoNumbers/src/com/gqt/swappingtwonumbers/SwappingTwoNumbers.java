package com.gqt.swappingtwonumbers;

public class SwappingTwoNumbers {

	public static void main(String[] args) {
		
		int a =10, b=20;
		
System.out.println("Before Swapping Two Numbers....." +a+" "+b);

   int t = a;
   a=b;
   b = t;
  
System.out.println("After Swapping Two Numbers....." +a+" "+b);
   
	}

}
